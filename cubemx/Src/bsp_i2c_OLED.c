/*
 * 输入当前时间戳，输出当前时间显示
 */
void OLED_ShowTime(void)
{
    time_t timestamp;
    struct tm lctime;
    uint8_t oledtime[14],*p;
    p=oledtime;

    timestamp = time(RT_NULL);
    lctime = *localtime(&timestamp);

    *p=0x30| ((lctime.tm_year-100)/10%10); p++;
    *p=0x30| ((lctime.tm_year-100)/1%10); p++;
    *p='/';p++;
    *p=0x30| ((lctime.tm_mon+1)/10%10); p++;
    *p=0x30| ((lctime.tm_mon+1)/1%10); p++;
    *p='/';p++;
    *p=0x30| ((lctime.tm_mday)/10%10); p++;
    *p=0x30| ((lctime.tm_mday)/1%10); p++;
    *p=' ';p++;
    *p=0x30| ((lctime.tm_hour)/10%10); p++;
    *p=0x30| ((lctime.tm_hour)/1%10); p++;
    *p=':';p++;
    *p=0x30| ((lctime.tm_min)/10%10); p++;
    *p=0x30| ((lctime.tm_min)/1%10); p++;
//    *p=':';p++;
//    *p=0x30| ((lctime.tm_sec)/10%10); p++;
//    *p=0x30| ((lctime.tm_sec)/1%10); p++;

    OLED_ShowStr(15,7,oledtime,1);
    return;
}

/**
  * 函数功能: OLED_DrawBMP，显示BMP位图
  * 输入参数: x0,y0 :起始点坐标(x0:0~127, y0:0~7)
  *           x1,y1 : 起点对角线(结束点)的坐标(x1:1~128,y1:1~8)
  * 返 回 值: 无
  * 说    明：无
  */
void OLED_DrawBMP(unsigned char x0,unsigned char y0,unsigned char x1,unsigned char y1,unsigned char BMP[])
{
    unsigned int j=0;
    unsigned char x,y;

  if(y1%8==0)
        y = y1/8;
  else
        y = y1/8 + 1;
    for(y=y0;y<y1;y++)
    {
        OLED_SetPos(x0,y);
    for(x=x0;x<x1;x++)
        {
            WriteDat(BMP[j++]);
        }
    }
}

