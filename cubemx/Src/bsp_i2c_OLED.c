//输入IP NTP地址
void OLED_ShowAdd(uint8_t x,uint8_t y,uint8_t *add,uint8_t page)
{
    if(page==0){
        for(int i=0;i<12;i++){
            if(*(add+i)==' '){
                OLED_ShowCN(x+i*10,y,0,X16_NULL);
            }
            else if(*(add+i)=='.'){
                OLED_ShowCN(x+i*10,y,0,X16_Poi);
            }
            else{
                OLED_ShowNumX16(x+i*10,y,0x0f&(*(add+i)));
            }
        }
    }
    else if(page==1){   //第二页
        for(int i=0;i<3;i++){
            if(*(add+12+i)==' '){
                OLED_ShowCN(x+i*10,y,0,X16_NULL);
            }
            else{
                OLED_ShowNumX16(x+i*10,y,0x0f&(*(add+12+i)));
            }
        }
    }
    else if(page==2){
        /* 端口号 */
        for(int i=0;i<4;i++){
            if(*(add+i)==' '){
                OLED_ShowCN(x+i*10+70,y,0,X16_NULL);
            }
            else{
                OLED_ShowNumX16(x+i*10+70,y,0x0f&(*(add+i)));
            }

        }
    }
    else{
        for(int i=0;i<12;i++){  //清理一行
            OLED_ShowCN(x+i*10,y,0,X16_NULL);
        }
    }

}

